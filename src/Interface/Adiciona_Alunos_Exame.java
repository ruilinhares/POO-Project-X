    private void jComboBox_ExamesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox_ExamesActionPerformed
        // TODO add your handling code here:
        if(jComboBox_Exames.getItemCount()<1){
            for(Exame ex:exames){
                jComboBox_Exames.addItem(ex.getDisciplina().getNome() + " (" + ex.getData().toString() + ")");
            }
        }
        else{
            if (jComboBox_Exames.getSelectedIndex()!= index){
                this.alunos = new ArrayList<>();
                this.index = jComboBox_Exames.getSelectedIndex();
                for (Aluno aluno : exames.get(index).getDisciplina().get_Alunos()) {
                    int v = 0;
                    for (Aluno alux : exames.get(index).getInscritos()) {
                        if (alux.getNum()==aluno.getNum() || (exames.get(index).getTipo().equals("Especial") && (aluno.getCurso().getDuracao()!=aluno.getMatriculas()) && (aluno.getRegime().equals("Normal") || aluno.getRegime().equals("Erasmus")))) v++;
                    }
                    if (v<1) this.alunos.add(aluno);
                    
                }
                jList1.setModel(new javax.swing.AbstractListModel<String>() {

                    @Override
                    public int getSize() { return alunos.size(); }
                    @Override
                    public String getElementAt(int i) {
                            return alunos.get(i).getNome()+" NÂº:"+ alunos.get(i).getNum();}
                });
            }
        }
    }//GEN-LAST:event_jComboBox_ExamesActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int[] indexs = jList1.getSelectedIndices();
        if(indexs.length > 0){
            for (int i = 0; i < indexs.length ; i++) {
                exames.get(index).getInscritos().add(alunos.get(i));
            }
            setVisible(false);
             Ficheiros_obj obj = new Ficheiros_obj();     
            try {
                obj.write_exames(exames);
            } catch (IOException ex) {
                Logger.getLogger(Adiciona_Alunos_Exame.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else jLabel_avisa.setText("Selecione algum aluno!");
    }//GEN-LAST:event_jButton1ActionPerformed

