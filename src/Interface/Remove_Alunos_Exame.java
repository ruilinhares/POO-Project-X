    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int[] indexs = jList1.getSelectedIndices();
        if(indexs.length > 0){
            for (int i = indexs.length-1; i >= 0 ; i--) {
                exames.get(index).getInscritos().remove(indexs[i]);
            }
            setVisible(false);
            Ficheiros_obj obj = new Ficheiros_obj();     
            try {
                obj.write_exames(exames);
            } catch (IOException ex) {
                Logger.getLogger(Remove_Alunos_Exame.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else jLabel_aviso.setText("Selecione algum aluno!");
    }//GEN-LAST:event_jButton1ActionPerformed


