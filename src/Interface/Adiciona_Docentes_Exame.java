    private void jComboBox_ExamesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox_ExamesActionPerformed
        // TODO add your handling code here:
        if(jComboBox_Exames.getItemCount()<1){
            for(Exame ex:exames){
                jComboBox_Exames.addItem(ex.getDisciplina().getNome() + " (" + ex.getData().toString() + ")");
            }
        }
        else{
            if (jComboBox_Exames.getSelectedIndex()!= index){
                this.docentes = new ArrayList<>();
                this.index = jComboBox_Exames.getSelectedIndex();
                Exame selExame = exames.get(index);
                Data dt = selExame.getData();
                double du = selExame.getDuracao();
                for (Curso curso : cursos){
                    for (Disciplina dis : curso.getDisciplinas()) {
                        for (Docente doc : dis.get_Docentes()){
                            int v = 0;
                            for (Docente auxDoc : docentes){
                                if (doc.getNum() == auxDoc.getNum()) v++;
                            }
                            for (Docente auxVigi : selExame.getVigilantes()){
                                if (doc.getNum() == auxVigi.getNum()) v++;
                            }
                            if (v==0){
                                for (Exame exame : exames) {
                                    for (Docente aux : docentes) {
                                        if (doc.getNum() == aux.getNum()){
                                            if (Data.horaLivre(exame.getData(), exame.getDuracao(),dt,du)) v++;
                                        }
                                    }
                                }
                                if (v==0) docentes.add(doc);
                            }
                            
                        }
                    }
                }
                jList1.setModel(new javax.swing.AbstractListModel<String>() {

                    @Override
                    public int getSize() { return docentes.size(); }
                    @Override
                    public String getElementAt(int i) {

                        return docentes.get(i).getNome()+" NÂº:"+ docentes.get(i).getNum(); }
                });
            }
        }
    }//GEN-LAST:event_jComboBox_ExamesActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int[] indexs = jList1.getSelectedIndices();
        if(indexs.length > 0){
            for (int i = 0; i < indexs.length ; i++) {
                exames.get(index).getVigilantes().add(docentes.get(i));
            }
            setVisible(false);
             Ficheiros_obj obj = new Ficheiros_obj();     
            try {
                obj.write_exames(exames);
            } catch (IOException ex) {
                Logger.getLogger(Adiciona_Docentes_Exame.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else jLabel_aviso.setText("Selecione algum aluno!");
    }//GEN-LAST:event_jButton1ActionPerformed

