    private void jButton_alunosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_alunosActionPerformed
        // TODO add your handling code here:
        this.inscritos = new ArrayList<>();
        int ver = 0;
        if (jRadioButton_eNormal.isSelected()){
            this.epoca = "Normal";
            ver++;
        }
        if (jRadioButton_eEspecial.isSelected()){
            this.epoca = "Especial";
            ver++;
        }
        if (jRadioButton_eRecurso.isSelected()){
            this.epoca = "Recurso";
            ver++;
        }
        
        if (inscritos != null && ver!=0){
            for (Aluno alux : criaExame.getDisSel().get_Alunos()){
                if (epoca.equals("Especial") && (alux.getCurso().getDuracao()!=alux.getMatriculas()) && (alux.getRegime().equals("Normal") || alux.getRegime().equals("Erasmus"))){
                    
                }else
                    inscritos.add(alux);
            }
            this.selAlunos = new sel_alunos(inscritos,epoca);
            GridBagConstraints c = new GridBagConstraints();
            c.gridx = 0;
            c.gridy = 0;

            jPanel_dinamico.add(selAlunos,c);
               
            jRadioButton_eNormal.setVisible(false);
            jRadioButton_eRecurso.setVisible(false);
            jRadioButton_eEspecial.setVisible(false);
            selVigilantes.setVisible(false);
            selFuncionarios.setVisible(false);
            selSalas.setVisible(false);
            criaExame.setVisible(false);
            selAlunos.setVisible(true);
        }
    }//GEN-LAST:event_jButton_alunosActionPerformed

    private void jButton_valiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_valiActionPerformed
        // TODO add your handling code here:
        this.inscritos = new ArrayList<>();
        this.inscritos = selAlunos.getSelecionados();
        
        this.vigilantes = new ArrayList<>();
        this.vigilantes = selVigilantes.getSelecionados();
        
        this.funcionarios = new ArrayList<>();
        this.funcionarios = selFuncionarios.getSelecionados();
        
        this.salas = new ArrayList<>();
        this.salas = selSalas.getSalas_disponiveis();
        
        if( inscritos.size()>0 && vigilantes.size()>0 && funcionarios.size()>0 && salas.size()>0 ){
            double duracao = criaExame.getDuracao();        
            Disciplina dis = criaExame.getDisSel();
            Docente responsavel = criaExame.getDocente();
            Data data = criaExame.getData();


            if ("Especial".equals(epoca)){
                this.novoExame = new ExameEspecial(dis,data,duracao,salas,responsavel, vigilantes, funcionarios, inscritos, new ArrayList<>());
            }
            else{
                this.novoExame = new ExameNormalRecurso(dis,data,duracao,salas,responsavel, vigilantes, funcionarios, inscritos, new ArrayList<>(), epoca);
            }
            this.exames.add(novoExame);
            
            Ficheiros_obj obj = new Ficheiros_obj();     
            try {
                obj.write_exames(exames);
            } catch (IOException ex) {
                Logger.getLogger(Add_Exame.class.getName()).log(Level.SEVERE, null, ex);
            }
            JOptionPane.showMessageDialog(null, "Exame Adicionado com Sucesso!");
            setVisible(false);
        }
        else{
            jLabel_curso.setText("OpcÃµes invalidas!");
        }
    }//GEN-LAST:event_jButton_valiActionPerformed

